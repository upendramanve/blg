<span style="font-weight: bold;">Lokhandi:</span>
Its flowers have a nice fragrance.
<br>
<span style="font-weight: bold;">Kokamb:</span>
The fruit of this tree is very useful in many ways, the skin of the fruit is turned after many operation into a kind of spice called "Amsol" which is frequently used in curries.
<br>
<span style="font-weight: bold;">Karvi: </span>
The branches of this are very much used in making thatches for poor men's cottages.
<br>
<span style="font-weight: bold;">Pandhari:</span>
It is very difficult to procure straight branches of this tree. The stick of this tree is very weighty and people believe that it keeps away all evil spirits.
<br>
<span style="font-weight: bold;">Pisa:</span>
It is in no way attractive in appearance. But its leaves have con­siderable tonic properties. Its leaves should be dried & powdered and taken every morning in boiled milk.
<br>
Besides these there are found various kinds of berries which are as sweet in the inside pulp as they are humble in the outward appearance.Mangos and Jackfruit are available and honey combs also can be found at Malet springand Hart Point side.
<br>
<br>
<span style="font-weight: bold;">Birds</span>
<br>
<br>
Whenever we visit a Point and sit in its thick shade the notes of the cuckoo and the Bulbul and various other birds provide heavenly music to our ears. The thicknesses of the trees hide the musicians from our view. But we crave to have a look at them. Matheran shows us numerous varieties of birds, which never meet our eyes in the cities, exhibiting different hues, different sizes and different voices, some sweet and some shrill.
<br>
<br>
<span style="font-weight: bold;">The Cuckoo : </span>
This is the Prince of all singing birds, and is heard singing all day during the monsoon.
<br>
<span style="font-weight: bold;">The Bulbul: </span>
This is also a favorite singing bird, and is very frequently found giving out its sweet notes on the way from the Louisa to the Porcupine Point at evening time.
<br>
<br>
There are many other birds also which seem to carry on a conversation with each other, one bird calling and another answering in a different tune.
<br>
<br>
Other birds are known, not for their voice, but for the beauty of their feathers. The most prominent amongst .these are the sunbird, the Robinson, the spotted Dove, the Parrot, the purple Honey sucker, and the Shriek. Shikra is a peculiar bird changing its colour now and again. His nest is a wonderful affair that might set even the best engineers thinking and his are said to possess medical properties.
<br>
<br>
The crow is markedly a rare bird here.
<br>
<br>
<span style="font-weight: bold;">Wild Animals</span>
<br>
<br>
Lions and panthers are hardly found here.
<br>
<br>
<span style="font-weight: bold;">Monkeys:</span>
  There are many numbers of monkeys here they are a very common sight, leaping from tree to tree or climbing the housetops. They are a restless race with bulky bodies and black faces, though" some have a red or a whitish tinge in the colour of their faces. It is a very funny spectacle to see their females clutching their little ones close under their bellies and jumping or their "Baba Lok" engaged in a hot quarrel. They never molest the passers by, though in some of them familiarity with mankind generates such an effrontery that they enter the houses and lay their hands on any thing they choose under the very nose of the occupants.
