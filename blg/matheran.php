					<p>Mr. Hugh Poyntz Malet, the then Collector of Thane, discovered Matheran (meaning "forest on the top") in May 1850. Lord Elphinstone, the then Governor of Mumbai laid foundations of the future development of Matheran as a hill station. This 2-foot narrow gauge hill railway, connecting Neral was opened to traffic in 1907.</p>
					<p>This hill railway was built by Sir Adamjee Peerbhoy, who formed a limited company with an authorised capital of ten lakh rupees divided into 2000 shares. The railway is 20 km. (12.6 miles) long and has a gauge of two feet with a ruling gradient of 1 in 20. The track zig zags up the side of the hill, bringing into view the full beauty of the Matheran Hill station. There are four pairs of train running between Neral and Matheran. However, during monsoon i.e. June to September, only one pair of train is run to open timings. </p>
