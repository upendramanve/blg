							<h3>The name of this hill station refers to the dense growth of jungle as its head. Matheran means a place topped by a jungle.</h3>
							<h4>By Rail/Train</h4>
							<p>Getting to Matheran is half the fun; It is well connected by rail both, from Mumbai and Pune up to Neral Junction Station, a station at the bottom of the hill. From Neral a small Toy Train (Narrow gauge) takes you through a zigzag route of about 21 kms. to Matheran in two hours time. It's a very scenic two hours ascent as the train winds its way around the steep slopes. Some marvelous valley scenes can be observed during this journey. Three small stations called Jumma Patti, Water Pipe and Aman Lodge are on the way to Matheran. </p>
							<p>The toy train can only be booked in advance from Mumbai or Pune which is a good idea in the high season (no reservations are accepted at Neral or Matheran but tickets go on sale 45 minutes before departure).</p>
							<p>Mumbai-Neral, 90 kms. Neral-Matheran,21 kms,</p>
							<p>From Mumbai CST, only a few of the Pune expresses stops at Neral Junction, including the Deccan Express and the Koyna Express. Most (but not all)of expresses from Mumbai stops at Karjat further down the line from Neral, from where you can backtrack on one of the frequent local trains. Alternatively, take a local Karjat bound train from Mumbai CST and get off at Neral.</p>
							<p>Pune-Neral, 120 kms. (Neral-Matheran mountain train)</p>
							<p>From Pune take a one of the few Mumbai bound expresses that stops at Neral (such as the Sahyadri Express) or one of the expresses which stops at Karjat and then get a local train from there up to Nerul.</p>
							<h4>By Road -> From Mumbai</h4>
							<p>Mumbai-Neral-Matheran, 108 kms.</p>
							<p>While going to Matheran by road, take Mumbai-Pune Highway(NH-4) upto Chowk Village and turn to Karjat road. From Karjat, the same road goes to Dasturi Naka, i.e. entrance of Matheran via Neral. </p>
							<p>Get on to the Mumbai Pune Expressway at Panvel (Near McDonalds). Get onto the expressway to avoid the congested road through Panvel. Take the first exit (Shedung/Khopoli Exit) that comes less than 5 minutes after you get onto the expressway at McDonalds. (Here you will need to Pay a Toll at the toll-booth for using the expressway). This exit gets you back onto the Old Mumbai-Pune Road. Follow directions to proceed towards Pune. Once again you will have to pay a toll for using the old Bombay Pune Road. (Toll in both places is about Rs.25 each).</p>
							<p>Follow the old Mumbai-Pune road for approximately 10 km. This will bring you to a crossing where a big green sign asks you to turn left for Karjat/Matheran. (Do not miss this crossing as otherwise you will end up in Khopoli. There is a large statue of Hindu god Shiva on right side of the old Mumbai-Pune Highway which can be seen from a long distance. The left turn about 50 metres before the statue).</p>
							<p>9 km After turning left you reach another crossing called Charphata Karjat where you need to turn left towards Neral.</p>
							<p>11 km After turning left, you will reach the bottom of the Matheran Hill ghat road.</p>
							<p>Turn left here and climb 7 km to the Matheran Dasturi Car park.</p>
							<h4>From Pune</h4>
							<p>Set off towards Mumbai on the Pune-Mumbai expressway. Get off the Pune-Mumbai Expressway at the Khopoli exit after passing Lonavla/Khandala and get onto the old Pune-Mumbai road. Approximately 18 km later, you turn right as per a big green sign, in Chouk town. Follow the signs from then on.</p>
							<p>One can also exit Pune-Mumbai NH4 at Haal Phata near Khopoli to enter SH 35 which will take you straight to the bottom of Matheran Ghat Road.</p>
							<h4>Car Parking and Entry </h4>
							<p>It is a steep and narrow road but well surfaced for the most part. When you reach the car park find a parking space in the designated area and park your car. The parking entrance fees and daily parking fees need to be paid here.</p>
							<p>No vehicles are allowed beyond this point. Matheran center is a 40-minute walk from Dasturi Car Park. Before you enter the limits of Matheran, Matheran Municipal council charges a fees of Rs 25 per adult and Rs 10 for children.</p>
							<p>Once you enter, red dusty pathways welcome you to the nature. Porters are available to carry luggage. You can enjoy the walks through the red pathways or ride on horse. Manual 'rickshaws'' are also available; each rickshaw is 'driven' by a couple of operators.</p>
							<p>Parking facilities are available at Dasturi Naka, Matheran. From this point into the town center, either a trek on foot or on horseback or by hand pulled rickshaws, as motor vehicles are prohibited within the hill-station area. During rainy season Train Services are not regular. Taxi takes about 20 minutes to reach Dasturi Naka from Neral. To go around Matheran, one has to walk or avail the services of horses or handcart (single seater).State Transport buses regularly ply from Mumbai and Pune. A taxi from Neral to Matheran takes about half hour to reach. Taxis stop about 2.5 km from the centre of the hill station.<strong>Vehicles of any kind are not permitted in the town, which makes it a very quiet, serene place.</strong></p>
